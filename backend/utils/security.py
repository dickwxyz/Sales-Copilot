def check_injection(text: str) -> tuple[bool, list[str]]:
    """
    检测输入文本是否包含提示注入关键词。

    Args:
        text: 用户输入的文本

    Returns:
        (is_attack: bool, matched_keywords: list[str])
    """
    if not text:
        return False, []

    matched = []
    for pattern, desc in LEAK_KEYWORDS:
        if re.search(pattern, text, re.IGNORECASE):
            matched.append(desc)

    return bool(matched), matched
